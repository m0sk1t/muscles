angular.module('MuscleMan', ['ngLocale', 'ui.calendar', 'ngFileUpload', 'ngRoute', '720kb.datepicker', 'froala', 'Services']).config(['$routeProvider',
	function($routeProvider) {
		$routeProvider.when('/', {
			templateUrl: 'tpl/main.tpl',
			controller: 'MainCtrl'
		}).when('/manager/signin', {
			templateUrl: 'tpl/manager_signin.tpl',
			controller: 'ManagerSignInCtrl'
		}).when('/manage/user/:id', {
			templateUrl: 'tpl/manage_user.tpl',
			controller: 'ManageUserCtrl'
		}).when('/manage/article/:id', {
			templateUrl: 'tpl/manage_article.tpl',
			controller: 'ManageArticleCtrl'
		}).when('/manage/contest/:id', {
			templateUrl: 'tpl/manage_contest.tpl',
			controller: 'ManageContestCtrl'
		}).when('/manage/competition/:id', {
			templateUrl: 'tpl/manage_competition.tpl',
			controller: 'ManageCompetitionCtrl'
		}).when('/manager/:id', {
			templateUrl: 'tpl/manager.tpl',
			controller: 'ManagerCtrl'
		}).when('/signup', {
			templateUrl: 'tpl/signup.tpl',
			controller: 'SignUpCtrl'
		}).when('/signin', {
			templateUrl: 'tpl/signin.tpl',
			controller: 'SignInCtrl'
		}).when('/user/:userid/album/:id', {
			templateUrl: 'tpl/album.tpl',
			controller: 'AlbumCtrl'
		}).when('/hobbies', {
			templateUrl: 'tpl/hobbies.tpl',
			controller: 'HobbiesCtrl'
		}).when('/articles', {
			templateUrl: 'tpl/articles.tpl',
			controller: 'ArticlesCtrl'
		}).when('/article/:id', {
			templateUrl: 'tpl/article.tpl',
			controller: 'ArticleCtrl'
		}).when('/competitions', {
			templateUrl: 'tpl/competitions.tpl',
			controller: 'CompetitionsCtrl'
		}).when('/competition/:id', {
			templateUrl: 'tpl/competition.tpl',
			controller: 'CompetitionCtrl'
		}).when('/contests', {
			templateUrl: 'tpl/contests.tpl',
			controller: 'ContestsCtrl'
		}).when('/contest/:id', {
			templateUrl: 'tpl/contest.tpl',
			controller: 'ContestCtrl'
		}).when('/dialogs', {
			templateUrl: 'tpl/dialogs.tpl',
			controller: 'DialogsCtrl'
		}).when('/favs', {
			templateUrl: 'tpl/favs.tpl',
			controller: 'FavsCtrl'
		}).when('/user/:id/photos/:photoid', {
			templateUrl: 'tpl/photos.tpl',
			controller: 'PhotosCtrl'
		}).when('/user/:id/videos/:videoid', {
			templateUrl: 'tpl/videos.tpl',
			controller: 'VideosCtrl'
		}).when('/options', {
			templateUrl: 'tpl/options.tpl',
			controller: 'OptionsCtrl'
		}).when('/search', {
			templateUrl: 'tpl/search.tpl',
			controller: 'SearchCtrl'
		}).when('/user/:id', {
			templateUrl: 'tpl/user.tpl',
			controller: 'UserCtrl'
		}).otherwise({
			redirectTo: '/'
		});
	}
]).run(function($rootScope) {
	console.log('App loaded!');
});
